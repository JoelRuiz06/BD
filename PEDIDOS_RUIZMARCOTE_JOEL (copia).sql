CREATE OR REPLACE PROCEDURE PROCESAR_PEDIDO(
	V_ID_PEDIDO NUMBER
)IS
	CONTADOR_STOCK NUMBER;
	CONTADOR_CANT NUMBER;
	SUFICIENTE EXCEPTION;
BEGIN
	SELECT STOCK INTO CONTADOR_STOCK FROM PRODUCTOS WHERE ID_PRODUCTO = V_ID_PEDIDO;
	SELECT CANTIDAD INTO CONTADOR_CANT FROM LINEA_PEDIDO WHERE ID_PRODUCTO = V_ID_PEDIDO;
	
	IF CONTADOR_STOCK > CONTADOR_CANT THEN
		CONTADOR_STOCK:= CONTADOR_STOCK - CONTADOR_CANT;
	ELSE
		RAISE SUFICIENTE;
	END IF;
	
	EXCEPTION
		WHEN SUFICIENTE THEN
		DMBS_OUTPUT.PUT_LINE('STOCK INSUFICIENTE PARA EL PRODCUTO CUYO ID ES: ' || V_ID_PRODUCTO);
END;

