	ID_LOG NUMBER(10),
	TABLA_AFECTADA VARCHAR2(100),
	OPERACION VARCHAR2(100),
	FECHA_EVENTO DATE,
	CONSTRAINT PK_AUDITAR_PEDIDOS PRIMARY KEY (ID_LOG)
);

CREATE SEQUENCE SEQ_AUDITARIA START WITH 1 INCREMENT BY 1;

CREATE OR REPLACE TRIGGER AUDITAR_PEDIDOS
BEFORE INSERT OR UPDATE OR DELETE ON PEDIDOS
FOR EACH ROW
DECLARE
  V_OPERACION VARCHAR2(20);
BEGIN
  IF INSERTING THEN
    V_OPERACION := 'INSERCION';
  ELSIF UPDATING THEN
    V_OPERACION := 'ACTUALIZACION';
  ELSE
    V_OPERACION := 'BORRADO';
  END IF;

  INSERT INTO LOG_AUDITORIA
  VALUES (SEQ_AUDITARIA.NEXTVAL, 'PEDIDOS', V_OPERACION, SYSDATE);
END;

