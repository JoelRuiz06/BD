CREATE OR REPLACE PROCEDURE AJUSTAR_PRECIO_PRODUCTO(
	V_ID_PRODUCTO IN OUT NUMBER,
	V_PORCENTAJE NUMBER,
	V_PRECIO_AJUSTADO OUT NUMBER
)IS
	CONTADOR NUMBER;
	PRECIO_ACTUAL NUMBER;
	EXISTE EXCEPTION;
BEGIN
	SELECT COUNT(*) INTO CONTADOR FROM PRODUCTOS WHERE ID_PRODUCTO = V_ID_PRODUCTO;
	IF V_ID_PRODUCTO = 1 THEN
		SELECT PRECIO INTO PRECIO_ACTUAL FROM PRODUCTOS WHERE ID_PRODUCTO = V_ID_PRODUCTO;
		PRECIO_AJUSTADO:= (V_PORCENTAJE / 100) * PRECIO_ACTUAL;
	ELSE
		RAISE EXISTE;
	END IF;
	
	EXCEPTION
		WHEN EXISTE THEN
		DMBS_OUTPUT.PUT_LINE('PRODUCTO NO EXISTE');
END;


