/* Realizar una función que devuelva un 1 si el nombre y apellido de la
persona que se le pasa por parámetro es un alumno, un dos si es un
profesor y un 0 si no está en la base de datos. */

CREATE OR REPLACE FUNCTION QUE_ES(
    V_NOMBRE   VARCHAR2,
    V_APELLIDO VARCHAR2
) RETURN NUMBER
IS
    CONTADOR NUMBER;
    RESULTADO NUMBER := 0;
BEGIN
    -- ES ALUMNO?
    SELECT COUNT(*) INTO CONTADOR
    FROM ALUMNO
    WHERE UPPER(NOMBRE) = UPPER(V_NOMBRE)
      AND UPPER(APELLIDO) = UPPER(V_APELLIDO);

	-- SI ME DA ALGO LA SELECT, ES QUE SI ES ALUMNO
    IF CONTADOR > 0 THEN
        RESULTADO := 1;
    ELSE
        -- ES PROFESOR?
        SELECT COUNT(*) INTO CONTADOR
        FROM PROFESOR
        WHERE UPPER(NOMBRE) = UPPER(V_NOMBRE)
          AND UPPER(APELLIDO) = UPPER(V_APELLIDO);

		-- SI ME DA ALGO LA SELECT, ES QUE SI ES PROFE
        IF CONTADOR > 0 THEN
            RESULTADO := 2;
        END IF;
    END IF;

    RETURN RESULTADO;

EXCEPTION
    WHEN OTHERS THEN
        RETURN 0;
END;


/* Crear un procedure que reciba como parámetro el nombre de una
titulación, el nombre de la asignatura, y el nombre y apellido del
profesor (en dos parámetros distintos), y que inserte esos datos en la
tabla de asignatura. Si se produce un error notificarlo. Los errores
que deben notificarse son:
	• No existe la titulación
	• No existe la personas
	• La persona no es un profesor
	• El nombre de la asignatura ya está en la base de datos. */

CREATE OR REPLACE PROCEDURE INSERTAR_ASIGNATURA(
    V_TITULACION VARCHAR2,
    V_ASIGNATURA VARCHAR2,
    V_NOMBRE     VARCHAR2,
    V_APELLIDO   VARCHAR2
)
IS
    COD_TITU NUMBER;
    COD_PROF NUMBER;
    CONTADOR NUMBER;
BEGIN
    -- EXISTE LA TITULACIÓN?
    SELECT COUNT(*) INTO CONTADOR
    FROM TITULACION
    WHERE UPPER(NOMBRE) = UPPER(V_TITULACION);

    IF CONTADOR = 0 THEN
        DBMS_OUTPUT.PUT_LINE('NO EXISTE LA TITULACIÓN');
        RETURN;
    ELSE
    	SELECT COD_TITULACION INTO COD_TITU
    	FROM TITULACION
    	WHERE UPPER(NOMBRE) = UPPER(V_TITULACION);
    END IF;

    -- EXISTE LA PERSONA COMO PROFESOR?
    SELECT COUNT(*) INTO CONTADOR
    FROM PROFESOR
    WHERE UPPER(NOMBRE) = UPPER(V_NOMBRE)
      AND UPPER(APELLIDO) = UPPER(V_APELLIDO);

    IF CONTADOR = 0 THEN
        -- y como alumno?
        SELECT COUNT(*) INTO CONTADOR
        FROM ALUMNO
        WHERE UPPER(NOMBRE) = UPPER(V_NOMBRE)
          AND UPPER(APELLIDO) = UPPER(V_APELLIDO);

        IF CONTADOR = 0 THEN
            DBMS_OUTPUT.PUT_LINE('LA PERSONA NO EXISTE');
        ELSE
            DBMS_OUTPUT.PUT_LINE('NO ES PROFESOR');
        END IF;

        RETURN;
    END IF;

    -- Obtener código del profesor para el insert y no salte el problemita del FK
    SELECT COD_PROFESOR INTO COD_PROF
    FROM PROFESOR
    WHERE UPPER(NOMBRE) = UPPER(V_NOMBRE)
      AND UPPER(APELLIDO) = UPPER(V_APELLIDO);

    -- YA EXISTE LA ASIGNATURA?
    SELECT COUNT(*) INTO CONTADOR
    FROM ASIGNATURA
    WHERE UPPER(NOMBRE) = UPPER(V_ASIGNATURA);

    IF CONTADOR > 0 THEN
        DBMS_OUTPUT.PUT_LINE('YA EXISTE LA ASIGNATURA');
        RETURN;
    END IF;

    -- INSERTAR ASIGNATURA
    INSERT INTO ASIGNATURA (COD_ASIGNATURA, NOMBRE, PRECIO, COD_TITULACION, COD_PROFESOR)
    VALUES (SEQ_ASIGNATURA.NEXTVAL, V_ASIGNATURA, 0, COD_TITU, COD_PROF);

    DBMS_OUTPUT.PUT_LINE('ASIGNATURA INSERTADA CORRECTAMENTE');

EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('ERROR DESCONOCIDO: ' || SQLERRM);
END;

