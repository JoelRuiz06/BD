--- Base de datos tablas

CREATE TABLE ALUMNO (
    COD_ALUMNO NUMBER(10),
    DNI        VARCHAR2(15),
    NOMBRE     VARCHAR2(30),
    APELLIDO   VARCHAR2(30),
    CONSTRAINT PK_ALUMNO PRIMARY KEY (COD_ALUMNO),
    CONSTRAINT UK_ALU_DNI UNIQUE (DNI)
);

CREATE TABLE PROFESOR (
    COD_PROFESOR NUMBER(10),
    NOMBRE       VARCHAR2(30),
    APELLIDO     VARCHAR2(30),
    CONSTRAINT PK_PROFESOR PRIMARY KEY (COD_PROFESOR)
);

CREATE TABLE TITULACION (
    COD_TITULACION NUMBER(10),
    NOMBRE         VARCHAR2(50),
    CONSTRAINT PK_TITULACION PRIMARY KEY (COD_TITULACION)
);

CREATE TABLE ASIGNATURA (
    COD_ASIGNATURA NUMBER(10),
    NOMBRE         VARCHAR2(50),
    PRECIO         NUMBER(10,2),
    COD_TITULACION NUMBER(10),
    COD_PROFESOR   NUMBER(10),
    CONSTRAINT PK_ASIGNATURA PRIMARY KEY (COD_ASIGNATURA),
    CONSTRAINT FK_ASIG_TITU FOREIGN KEY (COD_TITULACION) REFERENCES TITULACION(COD_TITULACION),
    CONSTRAINT FK_ASIG_PROF FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR(COD_PROFESOR)
);

CREATE TABLE MATRICULA (
    COD_MATRICULA  NUMBER(10),
    COD_ALUMNO     NUMBER(10),
    COD_ASIGNATURA NUMBER(10),
    CONSTRAINT PK_MATRICULA PRIMARY KEY (COD_MATRICULA),
    CONSTRAINT FK_MATRI_ALU FOREIGN KEY (COD_ALUMNO) REFERENCES ALUMNO(COD_ALUMNO),
    CONSTRAINT FK_MATRI_ASIG FOREIGN KEY (COD_ASIGNATURA) REFERENCES ASIGNATURA(COD_ASIGNATURA)
);

CREATE TABLE IMPARTE (
    COD_PROFESOR   NUMBER(10),
    COD_ASIGNATURA NUMBER(10),
    CONSTRAINT FK_IMP_PROF FOREIGN KEY (COD_PROFESOR) REFERENCES PROFESOR(COD_PROFESOR),
    CONSTRAINT FK_IMP_ASIG FOREIGN KEY (COD_ASIGNATURA) REFERENCES ASIGNATURA(COD_ASIGNATURA)
);


