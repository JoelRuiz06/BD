/* Realizar una función que devuelva un uno o un cero, si el alumno con
dni que se le pasa como argumento está matriculado en la asignatura
cuyo nombre se le pasa también como argumento. La función también
deberá tener un parámetro de entrada salida en donde se devuelva el
nombre y apellido del profesor que le da clase en esa asignatura, si el
alumno está matriculado. Procedure o bloque anónimo para comprobar que
funciona. */

CREATE OR REPLACE FUNCTION ESTA_MATRICULADO(
    V_DNI        VARCHAR2,
    V_ASIG       VARCHAR2,
    V_PROFESOR   IN OUT VARCHAR2
) RETURN NUMBER
IS
    CONTADOR  NUMBER;
    DEVUELVE  NUMBER := 0;
BEGIN
    -- ESTÁ MATRICULADO?
    SELECT COUNT(*) INTO CONTADOR
    FROM ALUMNO A
    JOIN MATRICULA M ON A.COD_ALUMNO = M.COD_ALUMNO
    JOIN ASIGNATURA S ON M.COD_ASIGNATURA = S.COD_ASIGNATURA
    WHERE A.DNI = V_DNI
      AND UPPER(S.NOMBRE) = UPPER(V_ASIG);

    IF CONTADOR > 0 THEN
        -- SI ESTÁ MATRICULADO, SACAR PROFESOR
        SELECT P.NOMBRE || ' ' || P.APELLIDO
        INTO V_PROFESOR
        FROM PROFESOR P
        JOIN IMPARTE I ON P.COD_PROFESOR = I.COD_PROFESOR
        JOIN ASIGNATURA S ON I.COD_ASIGNATURA = S.COD_ASIGNATURA
        WHERE UPPER(S.NOMBRE) = UPPER(V_ASIG);

        DEVUELVE := 1;
    END IF;

    RETURN DEVUELVE;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
    WHEN OTHERS THEN
        RETURN 0;
END;


