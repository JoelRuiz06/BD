parámetro el nombre y apellido de una persona, e inserte de forma
automática esa persona como alumno. */

CREATE OR REPLACE PROCEDURE INSERTAR_ALUMNO(
    V_NOMBRE   VARCHAR2,
    V_APELLIDO VARCHAR2
) IS
CONTADOR NUMBER;
EXISTE EXCEPTION;
BEGIN
	SELECT COUNT(*) INTO CONTADOR FROM ALUMNO A 
	WHERE A.NOMBRE=V_NOMBRE
		AND A.APELLIDO=V_APELLIDO;

	IF CONTADOR = 0 THEN
	    INSERT INTO ALUMNO (COD_ALUMNO, NOMBRE, APELLIDO)
	    VALUES (SEQ_ALUMNO.NEXTVAL, V_NOMBRE, V_APELLIDO);
	ELSE 
		RAISE EXISTE;
	END IF;

	EXCEPTION
		WHEN EXISTE THEN
		DBMS_OUTPUT.PUT_LINE('USUARIO ' || V_NOMBRE || ' YA EXISTE');
END;

BEGIN
	INSERTAR_ALUMNO('Marta','Lopez');
END;


