/* Realizar una función que reciba como parámetro el nombre y el apellido
de una persona, también debe recibir un parámetro que podrá ser un 1
(en ese caso debe insertarlo como un alumno) o un 2 (debe insertarlo
como profesor), y un parámetro de entrada salida en el que deberá
devolver el código del profesor o alumno insertado. La función deberá
devolver un 1 si se ha podido realizar la inserción, y un cero si ha
ocurrido algún error. */

CREATE OR REPLACE FUNCTION INSERTAR_PERSONA(
    V_NOMBRE   VARCHAR2,
    V_APELLIDO VARCHAR2,
    V_TIPO     NUMBER,
    V_CODIGO   IN OUT NUMBER
) RETURN NUMBER
IS
    CONTADOR NUMBER;
    YA_EXISTE EXCEPTION;
	RESULTADO NUMBER;
BEGIN
	RESULTADO:=0;

    -- INSERTAR ALUMNO
    IF V_TIPO = 1 THEN

        -- EXISTE?
        SELECT COUNT(*) INTO CONTADOR FROM ALUMNO
        WHERE NOMBRE = V_NOMBRE
        	AND APELLIDO = V_APELLIDO;

        -- NO EXISTE, LO CREO
        IF CONTADOR = 0 THEN
	        V_CODIGO := SEQ_ALUMNO.NEXTVAL;
	
	        -- INSERTAR
	        INSERT INTO ALUMNO (COD_ALUMNO, NOMBRE, APELLIDO)
	        VALUES (V_CODIGO, V_NOMBRE, V_APELLIDO);
		ELSE
			RAISE YA_EXISTE;
        END IF;
        
        RESULTADO:=1;


     -- INSERTAR PROFESOR
    ELSIF V_TIPO = 2 THEN

        -- EXISTE?
        SELECT COUNT(*) INTO CONTADOR
        FROM PROFESOR
        WHERE NOMBRE = V_NOMBRE
        	AND APELLIDO = V_APELLIDO;

        -- NO EXISTE, LO CREO
        IF CONTADOR = 0 THEN
	        -- GENERAR CÓDIGO CON SECUENCIA
	        V_CODIGO := SEQ_PROFESOR.NEXTVAL;
	
	        -- INSERTAR
	        INSERT INTO PROFESOR (COD_PROFESOR, NOMBRE, APELLIDO)
	        VALUES (V_CODIGO, V_NOMBRE, V_APELLIDO);
		ELSE
			RAISE YA_EXISTE;
        END IF;

        RESULTADO:=1;

    END IF;
    RETURN RESULTADO;


EXCEPTION
    WHEN YA_EXISTE THEN
        DBMS_OUTPUT.PUT_LINE('USUARIO ' || V_NOMBRE || ' YA EXISTE');
        RETURN RESULTADO;

    WHEN OTHERS THEN
        RETURN RESULTADO;
        
END;

