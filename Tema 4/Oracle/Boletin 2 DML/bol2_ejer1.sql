CREATE TABLE FABRICANTE
(
	CODIGO NUMBER(10),
	NOMBRE VARCHAR2(100),
	CONSTRAINT PK_FABRICANTE PRIMARY KEY (CODIGO)
);

CREATE TABLE PRODUCTO
(
	CODIGO NUMBER(10),
	NOMBRE VARCHAR2(100),
	PRECIO NUMBER(10,2),
	COD_FABRICANTE NUMBER(10),
	CONSTRAINT PK_PRODUCTO PRIMARY KEY (CODIGO),
	CONSTRAINT FK_PRODCUTO FOREIGN KEY (COD_FABRICANTE) REFERENCES FABRICANTE(CODIGO)
);

