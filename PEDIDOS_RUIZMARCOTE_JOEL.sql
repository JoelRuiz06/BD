CREATE TABLE PEDIDOS(
	ID_PEDIDO NUMBER(10),
	FECHA_PEDIDO DATE,
	TOTAL_PEDIDO NUMBER(10),
	CONSTRAINT PK_PEDIDOS PRIMARY KEY (ID_PEDIDO)
);

CREATE TABLE PRODUCTOS(
	ID_PRODUCTO NUMBER(10),
	NOMBRE VARCHAR2(100),
	PRECIO NUMBER(10),
	STOCK NUMBER(10),
	CONSTRAINT PK_PRODUCTOS PRIMARY KEY (ID_PRODUCTO)
);

CREATE TABLE LINEAS_PEDIDO(
	ID_LINEA NUMBER(10),
	ID_PEDIDO NUMBER(10),
	ID_PRODUCTO NUMBER(10),
	CANTIDAD NUMBER(10),
	CONSTRAINT PK_LINEAS_PEDIDOS PRIMARY KEY (ID_LINEA),
	CONSTRAINT FK_LINEAS_PEDIDOS FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDOS (ID_PEDIDO),
	CONSTRAINT FK2_LINEAS_PEDIDOS FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTOS (ID_PRODUCTO)
);

